package ch01.sec02;

public class VariableDeclarationExample {
    public static void main(String[] args) {
        // (1) 정수형 변수 value1 선언
        int value1;

        // (2) value1 변수에 10 저장
        value1 = 10;
        
        // (3) value1 출력
        System.out.println(value1);

        // (4) 정수형 변수 value2 선언과 동시에 20으로 초기화
        int value2 = 20;
        
        // (5) value2 출력
        System.out.println(value2);
    }
}
