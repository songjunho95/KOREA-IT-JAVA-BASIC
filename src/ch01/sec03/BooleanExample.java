package ch01.sec03;

public class BooleanExample {
    public static void main(String[] args) {
        // 논리 타입 (boolean type)
        boolean isOn = true;
        boolean isMan = false;
        System.out.println("isOn == " + isOn);
        System.out.println("isMan == " + isMan);

        // 논리 타입 변수명 규칙
        //  접두사에 is, has, was 를 붙여서 사용합니다.
        boolean isWeekend = false;
        boolean isRun = false;
        boolean hasA = true;

        ////////////////// 연습 //////////////////

        // boolean 타입의 변수 animal 에 true 를 대입하세요.
        boolean isAnimal = true;
        System.out.println("isAnimal == " + isAnimal);

        //////////////////////////////////////////

    }
}
